COMMENT ON COLUMN t_cov_x_filetypes.coveragerule_id IS 'Id of referenced rule. References id_coveragerule on t_cov_rules table.' ;
COMMENT ON COLUMN t_cov_x_filetypes.filetype_id IS 'Id of referenced file type. References id_filetype on t_filetypes table.' ;


